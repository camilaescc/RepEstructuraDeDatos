//se crea la clase TriangleArea
public class TriangleArea {
    //se crea el metodo calculateArea en double y recibe dos paramatros
    public double calculateArea(double base, double height) {
        //regresa la operacion que daria como resultado el area de un triangulo en double
        return (base*height)/2;
    }
    
}
